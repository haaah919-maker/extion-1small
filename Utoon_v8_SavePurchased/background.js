// Persistent pending-inject store: tabId -> targetUrl
const pendingInjects = {};

chrome.runtime.onMessage.addListener((message, sender, sendResponse) => {
    if (message.action === "auto_inject") {
        const tabId = message.tabId;
        // Store the pending inject so the tab listener can fire it
        pendingInjects[tabId] = true;
        sendResponse({ ok: true });
    }
    return true;
});

// Persistent tab listener — runs for every tab update in background
chrome.tabs.onUpdated.addListener((tabId, info, tab) => {
    if (info.status !== 'complete') return;
    if (!pendingInjects[tabId]) return;

    // Check it's still a utoon chapter page
    if (!tab.url || !tab.url.includes('utoon.net')) {
        delete pendingInjects[tabId];
        return;
    }

    delete pendingInjects[tabId];

    // Inject reader_logic.js directly (no GitHub fetch — reliable & has our latest code)
    chrome.scripting.executeScript({
        target: { tabId: tabId },
        files: ['reader_logic.js']
    }).catch(err => {
        // Fallback to bundle if reader_logic fails
        chrome.scripting.executeScript({
            target: { tabId: tabId },
            files: ['bundle_reader.js']
        }).catch(() => {});
    });
});
